    private void btnAgregarDepartamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarDepartamentoActionPerformed
        int id;
        String departamento;
        
        try{
            id = Integer.parseInt(txfNoDepartamento.getText());
            departamento = txfDepartamento.getText();
            
            qDepartamentos.agregarDepartamento(id, departamento);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }finally{
            actualizarTabla();
            limpiar();
        }
        
    }//GEN-LAST:event_btnAgregarDepartamentoActionPerformed

    private void btnEditarDepartamentosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarDepartamentosActionPerformed
        try{
            if(btnEditarDepartamentos.getText().equals("Editar")){
                btnEditarEditar();
            }else if(btnEditarDepartamentos.getText().equals("Siguiente")){
                btnEditarSiguiente();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        actualizarTabla();
    }//GEN-LAST:event_btnEditarDepartamentosActionPerformed

    private void btnEliminarDepartamentosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarDepartamentosActionPerformed
        try{
            int row = tblDepartamentos.getSelectedRow();
            int idDpto = Integer.parseInt(tblDepartamentos.getModel().getValueAt(row,0).toString());
            int opcion=JOptionPane.showConfirmDialog(null,"¿Realmente desea eliminar el registro?","Alerta", JOptionPane.YES_NO_OPTION);

            if(opcion==JOptionPane.YES_OPTION){
                if(qDepartamentos.eliminarDepartamento(idDpto)){
                    JOptionPane.showMessageDialog(null, "Registro eliminado");
                }else{
                    JOptionPane.showMessageDialog(null, "Error");
                }
            }else{
                JOptionPane.showConfirmDialog(null, "Operacion abortada");
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        actualizarTabla();
    }//GEN-LAST:event_btnEliminarDepartamentosActionPerformed

    private void btnEditarEditar(){
        int row = tblDepartamentos.getSelectedRow();
        int idDpto = Integer.parseInt(tblDepartamentos.getModel().getValueAt(row,0).toString());
        String dpto = tblDepartamentos.getModel().getValueAt(row,1).toString();
        
        txfNoDepartamento.setText(String.valueOf(idDpto));
        txfDepartamento.setText(dpto);
        
        tblDepartamentos.setEnabled(false);
        btnSalir.setEnabled(false);
        btnEliminarDepartamentos.setEnabled(false);
        btnAgregarDepartamento.setEnabled(false);
        
        btnEditarDepartamentos.setText("Siguiente");
        
        idDptoActual = idDpto;
        
        txfNoDepartamento.requestFocus();
        
    }
    
    private void btnEditarSiguiente(){
        int opcion=JOptionPane.showConfirmDialog(null,"¿Desea efectuar los cambios?","Alerta", JOptionPane.YES_NO_OPTION);
        if(opcion==JOptionPane.YES_OPTION){
            if(qDepartamentos.modificarDepartamento(idDptoActual, Integer.parseInt(txfNoDepartamento.getText()), txfDepartamento.getText())){
                JOptionPane.showMessageDialog(null, "Cambios efectuados");
            }else{
                JOptionPane.showMessageDialog(null, "Error");
            }
        }else{
            JOptionPane.showConfirmDialog(null, "Operacion abortada");
        }
        tblDepartamentos.setEnabled(true);
        btnSalir.setEnabled(true);
        btnEliminarDepartamentos.setEnabled(true);
        btnAgregarDepartamento.setEnabled(true);
        
        limpiar();
        
        idDptoActual=0;
        btnEditarDepartamentos.setText("Editar");
    }
    
    public void limpiar(){
        txfNoDepartamento.setText("");
        txfDepartamento.setText("");
    }
    
    
