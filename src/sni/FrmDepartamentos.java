    public DefaultTableModel llenarDepartamentos(){
        List<Departamentos> departamentos = new ArrayList<Departamentos>();
        DefaultTableModel dtmModelo = new DefaultTableModel();
        
        dtmModelo.addColumn("No.");
        dtmModelo.addColumn("Departamento");
        
        departamentos = qDepartamentos.mostrarDepartamentos();
        
        int i=0;
        for(Departamentos dps:departamentos){
            dtmModelo.addRow(new Object[]{});
            dtmModelo.setValueAt(dps.getId(), i, 0);
            dtmModelo.setValueAt(dps.getDepartamento(), i, 1);
            i++;
        }
        return dtmModelo;
    }

