    private void btnEditarMunicipiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarMunicipiosActionPerformed
        try{
            if(btnEditarMunicipios.getText().equals("Editar")){
                btnEditarEditar();
            }else if(btnEditarMunicipios.getText().equals("Siguiente")){
                btnEditarSiguiente();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        actualizarTabla();
    }//GEN-LAST:event_btnEditarMunicipiosActionPerformed

    private void btnEliminarMunicipiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarMunicipiosActionPerformed
        try{
            int row = tblMunicipios.getSelectedRow();
            int idMun = Integer.parseInt(tblMunicipios.getModel().getValueAt(row,0).toString());
            int opcion=JOptionPane.showConfirmDialog(null,"Â¿Realmente desea eliminar el registro?","Alerta", JOptionPane.YES_NO_OPTION);

            if(opcion==JOptionPane.YES_OPTION){
                if(qMunicipios.eliminarMunicipio(idMun)){
                    JOptionPane.showMessageDialog(null, "Registro eliminado");
                }else{
                    JOptionPane.showMessageDialog(null, "Error");
                }
            }else{
                JOptionPane.showConfirmDialog(null, "Operacion abortada");
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        actualizarTabla();
    }//GEN-LAST:event_btnEliminarMunicipiosActionPerformed

    private void btnEditarEditar(){
        int row = tblMunicipios.getSelectedRow();
        int idMun = Integer.parseInt(tblMunicipios.getModel().getValueAt(row,0).toString());
        String mun = tblMunicipios.getModel().getValueAt(row,1).toString();
        String dep = tblMunicipios.getModel().getValueAt(row,2).toString();
        
        txfNoMunicipio.setText(String.valueOf(idMun));
        txfMunicipio.setText(mun);
        cmbDepartamento.setSelectedItem(dep);
        
        tblMunicipios.setEnabled(false);
        btnSalir.setEnabled(false);
        btnEliminarMunicipios.setEnabled(false);
        btnAgregar.setEnabled(false);
        
        btnEditarMunicipios.setText("Siguiente");
        
        idMunActual = idMun;
        
        txfNoMunicipio.requestFocus();
        
    }
    
    private void btnEditarSiguiente(){
        int opcion=JOptionPane.showConfirmDialog(null,"Â¿Desea efectuar los cambios?","Alerta", JOptionPane.YES_NO_OPTION);
        if(opcion==JOptionPane.YES_OPTION){
            if(qMunicipios.modificarMunicipio(idMunActual, Integer.parseInt(txfNoMunicipio.getText()), cmbDepartamento.getSelectedItem().toString(), txfMunicipio.getText())){
                JOptionPane.showMessageDialog(null, "Cambios efectuados");
            }else{
                JOptionPane.showMessageDialog(null, "Error");
            }
        }else{
            JOptionPane.showConfirmDialog(null, "Operacion abortada");
        }
        tblMunicipios.setEnabled(true);
        btnSalir.setEnabled(true);
        btnEliminarMunicipios.setEnabled(true);
        btnAgregar.setEnabled(true);
        
        limpiar();
        
        idMunActual=0;
        btnEditarMunicipios.setText("Editar");
    }
    
    
    public void limpiar(){
        txfMunicipio.setText("");
        txfNoMunicipio.setText("");
        cmbDepartamento.setSelectedIndex(0);
    }
