    public DefaultTableModel llenarMunicipio(){
        List<Municipios> municipios = new ArrayList<Municipios>();
        DefaultTableModel dtmModelo = new DefaultTableModel();
        
        dtmModelo.addColumn("No.");
        dtmModelo.addColumn("Municipio");
        dtmModelo.addColumn("Departamento");
        
        municipios = qMunicipios.mostrarMunicipiosDepto();
        
        int i=0;
        for(Municipios mps:municipios){
            dtmModelo.addRow(new Object[]{});
            dtmModelo.setValueAt(mps.getIdMunicipio(), i, 0);
            dtmModelo.setValueAt(mps.getMunicipio(), i, 1);
            dtmModelo.setValueAt(mps.getDepartamento(), i, 2);
            i++;
        }
        return dtmModelo;
    }
    
    public void llenarDepartamentos(){
        modeloDepartamento.addElement("--Selecciones Departamento--");
        listaDepartamentos = qDepartamentos.mostrarDepartamentos();
        for(Departamentos d:listaDepartamentos){
            modeloDepartamento.addElement(d.getDepartamento());
        }
        cmbDepartamento.setModel(modeloDepartamento);
    }

